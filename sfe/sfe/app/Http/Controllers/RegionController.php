        //
        $Region = new Region();
        $Region->nom = $request->input('nom');
        $Region->save();
        $villes = Ville::all();
        $regions = Region::all();
        return view('setting')->with('villes', $villes)->with('regions', $regions)->with('message', "region created");
    }

